input = input.split("\n")

h = len(input)
w = len(input[0])


def search_direction(r, c, dr, dc, s):
    for e in s:
        if r >= h or c >= w or r < 0 or c < 0:
            return False
        if e == input[r][c]:
            r += dr
            c += dc
            continue
        else:
            return False
    return True


def search(r, c, s):
    return sum(
        [
            search_direction(r, c, -1, 0, s),  # n
            search_direction(r, c, -1, -1, s),  # nw
            search_direction(r, c, -0, -1, s),  # w
            search_direction(r, c, 1, -1, s),  # sw
            search_direction(r, c, 1, 0, s),  # s
            search_direction(r, c, 1, 1, s),  # se
            search_direction(r, c, 0, 1, s),  # e
            search_direction(r, c, -1, 1, s),  # ne
        ]
    )


def big_search(s):
    for r, row in enumerate(input):
        for c, col in enumerate(row):
            if col == "X":
                yield search(r, c, s)


result = sum(big_search("XMAS"))

print(f"day4, part1 = {result}")


def mas_search():
    for r, row in enumerate(input):
        for c, col in enumerate(row):
            if col == "A":
                # search from top-left:
                if (
                    search_direction(r - 1, c - 1, 1, 1, "MAS")
                    + search_direction(r - 1, c - 1, 1, 1, "SAM")
                ) and (
                    (
                        search_direction(r - 1, c + 1, 1, -1, "MAS")
                        + search_direction(r - 1, c + 1, 1, -1, "SAM")
                    )
                ):
                    yield 1


result = sum(mas_search())

print(f"day4, part2 = {result}")
